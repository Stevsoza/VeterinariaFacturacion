    private void LimpiarTabla() {

        try {
            modelo = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };

            tblClientes.setModel(modelo);
            modelo.addColumn("ID");
            modelo.addColumn("Nombre");
            modelo.addColumn("Primer apellido");
            modelo.addColumn("Segundo apellido");
            modelo.addColumn("Fecha nacimiento");
            modelo.addColumn("Telef√≥no");
            modelo.addColumn("Email");
        } catch (Exception ex) {
            throw ex;
        }

    }

    private void cargarDatos(String condicion) throws Exception {
        try {
            LnCliente logica = new LnCliente();
            List<Cliente> lista;
            LimpiarTabla();
            Object[] fila = new Object[7];
            lista = logica.ListarRegistros(condicion);
            for (Cliente cliente : lista) {
                fila[0] = cliente.getId_cliente();
                fila[1] = cliente.getNombre();
                fila[2] = cliente.getApellido1();
                fila[3] = cliente.getApellido2();
                fila[4] = cliente.getFechaNacimiento();
                fila[5] = cliente.getTelefono();
                fila[6] = cliente.getEmail();
                modelo.addRow(fila);
            }
        } catch (Exception ex) {
            throw ex;
        }

    }

