    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if(tblClientes.getSelectedRow()>-1){
            int fila = tblClientes.getSelectedRow();
            int codigo = (int)tblClientes.getValueAt(fila, 0);
            try{
                LnCliente logica = new LnCliente();
                String condicion = "id_cliente = "+codigo;
                Cliente cliente;
                cliente = logica.ObtenerRegistro(condicion);
                
                if(cliente.isExiste()){
                    modificarClienteCRUD modificar = new modificarClienteCRUD(null, true, cliente);
                     modificar.addWindowListener(new WindowAdapter() {
                     
                         @Override
                         public void windowClosed(WindowEvent e){
                             try{
                                 cargarDatos("");
                             }catch(Exception ex){
                                 JOptionPane.showMessageDialog(null, "Error"+ex.getMessage());
                             }
                         }
                     });
                    modificar.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(this, "No existe el cliente");
                }
                
                
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Error: "+ex.getMessage());
            }
            
        }else{
            JOptionPane.showMessageDialog(this,"Debe seleccionar un cliente");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        // TODO add your handling code here:
        if(tblClientes.getSelectedRow()>-1){
            
            int resultado;
            int fila = tblClientes.getSelectedRow();
            int codigo = (int)tblClientes.getValueAt(fila, 0);
            try{
                LnCliente logica = new LnCliente();
                String condicion = "id_cliente = "+codigo;
                Cliente cliente;
                cliente = logica.ObtenerRegistro(condicion);
                
                if(cliente.isExiste()){
                    resultado = logica.Eliminar(cliente);
                    if(resultado>0){
                        cargarDatos("");
                        JOptionPane.showMessageDialog(this, "Cliente eliminado");
                        
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "No existe el cliente");
                }
                
                
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Error: "+ex.getMessage());
            }
            
        }else{
            JOptionPane.showMessageDialog(this,"Debe seleccionar un cliente");
        }
    }//GEN-LAST:event_btnEliminar1ActionPerformed

